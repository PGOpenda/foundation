                <h2>
                    What is liver cancer?
                 </h2>
                 <p> 
                     Liver cancer (also known as hepatic cancer, primary hepatic cancer, or primary hepatic malignancy) is a type of cancer 
                     that begins in the liver. Liver cancer can be primary, meaning it begins in the liver. Hepatic metastasis, or secondary,
                     meaning it travels from another part of the body to the liver. Of the two types of liver cancer, liver metastasis is the 
                     most common. 
                 </p>
            </div>
            <img src="{liver_cancer_illustration}" alt="Liver Cancer Illustration" height="250">
        </div>

        <h2>Classifications of Liver Cancer</h2>
        <p>
            In addition to the liver's parenchyma, additional components like the bile duct, blood arteries, and immune cells can also cause liver cancer.
            <br>
        </p>
        <ol>
            <li>
                <strong>Hepatocellular carcinoma </strong> <br>
                Hepatocellular carcinoma (HCC) is the most common type of liver cancer, making up around 75% of all primary liver malignancies . Hepatocytes, 
                which are liver cells, can develop into cancerous cells to cause HCC.
            </li>
            <li>
                <strong>Intrahepatic cholangiocarcinoma</strong> <br>
                About 6% of primary liver tumors are bile duct malignancies, including cholangiocarcinoma and cholangiocellular cystadenocarcinoma. 
                An epithelial malignancy of the intrahepatic biliary tree branches is known as intrahepatic cholangiocarcinoma (CCA). 
                The second most common cause of primary liver cancer is intrahepatic CCA.
            </li>
            <li>
                <strong>Angiosarcoma and hemangiosarcoma</strong> <br>
                These are rare and aggressive liver tumors, yet they are the third most prevalent, accounting for 0.1-2.0% of all primary liver cancers. 
                Angiosarcoma and hemangiosarcoma of the liver develop from the blood vessel's endothelial layer. These tumors have bad outcomes because 
                they grow quickly and spread easily. 
            </li>
            <li>
                <strong>Hepatoblastoma</strong> <br>
                Hepatoblastoma is a type of cancer caused by immature liver cells. It is a rare malignant tumor that typically occurs in children, accounting 
                for around 1% of all malignancies in children and 79% of all primary liver tumors under the age of fifteen.
            </li>
            <li>
                <strong>Metastasis to liver </strong> <br>
                Many malignancies seen in the liver are not real liver cancers, but rather cancers from other parts of the body that have metastasized to the liver.
                 Frequently, the site of genesis is the digestive tract.
            </li>
        </ol>

